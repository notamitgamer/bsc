#include <stdio.h>

void swap(int *a, int *b)
{
    int temp = *a;
    *a = *b;
    *b = temp;
}

int main()
{
    int a, b;
    printf("Enter A and B: ");
    scanf("%d %d", &a, &b);
    printf("\nBefore swap A: %d, B: %d", a, b);
    swap(&a, &b);
    printf("\nAfter swap A: %d, B: %d", a, b);
    return 0;
}